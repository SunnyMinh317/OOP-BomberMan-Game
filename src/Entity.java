import javax.swing.*;

public class Entity {
    public int x;
    public int y;

    public Entity() {
        x = 100;
        y = 100;
    }

    public int getX() {
        return x;
    }

    public void setX(int _x) {
        x = _x;
    }

    public int getY() {
        return y;
    }

    public void setY(int _y) {
        y = _y;
    }
}
